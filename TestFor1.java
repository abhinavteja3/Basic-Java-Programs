class TestFor1
{
public static void main(String args[])
{

int i,j,k;




	for(i=0;i<2;i++)
	{
	for(j=0;j<2;j++)	
	{
		for(k=0;k<2;k++)
		{
		System.out.print(i+" ");
		System.out.print(j+" ");
		System.out.println(k);
		}	
		System.out.println();
	}
		System.out.println();
	}
	

		
}
}
